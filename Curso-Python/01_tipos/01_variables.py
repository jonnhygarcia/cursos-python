# tipos de datos en python: int, float, str, bool
# int: numeros enteros, sin decimales.
# float: numeros con decimales.
# str: cadenas de texto, entre comillas simples o dobles.
# bool: booleanos, True o False.
# asignacion de variables con diferentes tipos de datos.
palabra = "Python"  # string o "str"
numero = 3  # int "Entero"
alumno = 5000  # int "Entero"
puntuaje = 9.9  # float "Decimal"
